import os
import google.generativeai as genai

genai.configure(api_key=os.getenv("GOOGLEAI_API_KEY"))
model = genai.GenerativeModel("gemini-1.5-flash")
disclaimer = "\n_Generated by AI (Google Gemini 1.5)_"


def explainSong(lyrics):
    response = ""
    try:
        response = model.generate_content(
            ["Explain this hymn in up to 7 lines", lyrics]
        ).text
        response += disclaimer
    except:
        response = "_This feature is temporarily unavailable_"
    return response
